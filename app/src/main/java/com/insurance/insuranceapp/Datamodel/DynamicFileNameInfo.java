public class DynamicFileNameInfo {

    private String document_name;
    private String attach_file_name;
    private String attach_doc_name_id;
    private String case_module_id;



    public String getDocument_name() {
        return document_name;
    }

    public void setDocument_name(String document_name) {
        this.document_name = document_name;
    }

    public String getAttach_file_name() {
        return attach_file_name;
    }

    public void setAttach_file_name(String attach_file_name) {
        this.attach_file_name = attach_file_name;
    }

    public String getAttach_doc_name_id() {
        return attach_doc_name_id;
    }

    public void setAttach_doc_name_id(String attach_doc_name_id) {
        this.attach_doc_name_id = attach_doc_name_id;
    }

    public String getCase_module_id() {
        return case_module_id;
    }

    public void setCase_module_id(String case_module_id) {
        this.case_module_id = case_module_id;
    }
}
